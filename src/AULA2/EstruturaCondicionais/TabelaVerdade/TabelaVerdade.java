public class TabelaVerdade {
    // F = FALSO
    // V =  VERDADEIRO
    // && - (E) - (AND)
    // V && V = V
    // V && F = F
    // F && V = F
    // F && F = F
    // V && V && V && F = F
    // UNICO F DEIXA TUDO FALSO NA OPERAÇÃO (&&)

    // F = FALSO
    // V =  VERDADEIRO
    // || - (E) - (AND)
    // V || V = V
    // V || F = V
    // F || V = V
    // F || F = F
    // V || V || V || F = V
    // UNICO V DEIXA TUDO VERDADEIRO NA OPERAÇÃO (||)
    // SO VAI SER FALSO SE TUDO FOR F
}
